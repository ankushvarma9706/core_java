package com.stringexamples.cfs;

public class Example5 {
    public static void main(String[] args)
    {
        int a = 10 ;
//        double = 10 .5  ; invalid
        switch (a)
        {
            case 10 :
                System.out.println("hello") ;
            case 11 :
                System.out.println("hi") ;
            case 12 :
                System.out.println("bye") ;
                break;
        }
    }
}


